public class SimpleCalculator {
    
    protected static double add(double num1, double num2) {
        return num1 + num2;
    }
    
    protected static double sub(double num1, double num2) {
        return num1 - num2;
    }    
    
    protected static double div(double num1, double num2) {
        return num1 / num2;
    }
    
    protected static double multiply(double num1, double num2) {
        return num1 * num2;
    }
}
